public class DateData {
    String english_month;
    String english_week;
    String english_day;
    String trad_month;
    String trad_week;
    String trad_day;
    String onwa_izu_asaa;

    public DateData() {
    }

    public String getEnglish_month() {
        return english_month;
    }

    public void setEnglish_month(String english_month) {
        this.english_month = english_month;
    }

    public String getEnglish_week() {
        return english_week;
    }

    public void setEnglish_week(String english_week) {
        this.english_week = english_week;
    }

    public String getEnglish_day() {
        return english_day;
    }

    public void setEnglish_day(String english_day) {
        this.english_day = english_day;
    }

    public String getTrad_month() {
        return trad_month;
    }

    public void setTrad_month(String trad_month) {
        this.trad_month = trad_month;
    }

    public String getTrad_week() {
        return trad_week;
    }

    public void setTrad_week(String trad_week) {
        this.trad_week = trad_week;
    }

    public String getTrad_day() {
        return trad_day;
    }

    public void setTrad_day(String trad_day) {
        this.trad_day = trad_day;
    }

    public String getOnwa_izu_asaa() {
        return onwa_izu_asaa;
    }

    public void setOnwa_izu_asaa(String onwa_izu_asaa) {
        this.onwa_izu_asaa = onwa_izu_asaa;
    }
}
